Q.23 Write query to return the email, first name, last name, & Genre of all Rock Music listeners. 
Return your list ordered alphabetically by email starting with A. 

SELECT DISTINCT email AS Email,first_name AS FirstName, last_name AS LastName, genre.name AS Name
FROM customer
JOIN invoice ON invoice.customer_id = customer.customer_id
JOIN invoiceline ON invoiceline.invoice_id = invoice.invoice_id
JOIN track ON track.track_id = invoiceline.track_id
JOIN genre ON genre.genre_id = track.genre_id
WHERE genre.name LIKE 'Rock'
ORDER BY email;

Q.24 Return all the track names that have a song length longer than the average song length. 
Return the Name and Milliseconds for each track. Order by the song length with the longest songs listed first.

SELECT name,miliseconds
FROM track
WHERE miliseconds > (
	SELECT AVG(miliseconds) AS avg_track_length
	FROM track )
ORDER BY miliseconds DESC;

Q.25 Find artists who have more than 10 tracks.

WITH artist_tracks AS
(
    SELECT ar.name,
           COUNT(t.track_id) AS total_tracks
    FROM artist ar
    JOIN album alb
    ON ar.artist_id = alb.artist_id
    JOIN track t
    ON alb.album_id = t.album_id
    GROUP BY ar.name
)

SELECT *
FROM artist_tracks
WHERE total_tracks > 10
ORDER BY total_tracks DESC;

Q.26 Number customers within each country based on their spending.

SELECT c.country,
       c.first_name,
       c.last_name,
       SUM(i.total) AS total_spent,
       ROW_NUMBER() OVER(
           PARTITION BY c.country
           ORDER BY SUM(i.total) DESC
       ) AS row_num
FROM customer c
JOIN invoice i
ON c.customer_id = i.customer_id
GROUP BY c.country, c.customer_id, c.first_name, c.last_name;
	
	
