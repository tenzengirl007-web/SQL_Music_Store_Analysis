Q.1 Find customers who spent more than the average invoice amount.

SELECT c.customer_id,
       c.first_name,
       c.last_name,
       SUM(i.total) AS total_spent
FROM customer c
JOIN invoice i
ON c.customer_id = i.customer_id
GROUP BY c.customer_id, c.first_name, c.last_name
HAVING SUM(i.total) >
(
SELECT AVG(total)
FROM invoice
)
ORDER BY total_spent DESC;

Q.2 Find the total revenue generated by each employee.

